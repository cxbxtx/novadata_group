def temperature_converter():
    """–ö–æ–Ω–≤–µ—Ä—Ç–µ—Ä —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä –º–µ–∂–¥—É –¶–µ–ª—å—Å–∏–µ–º, –§–∞—Ä–µ–Ω–≥–µ–π—Ç–æ–º –∏ –ö–µ–ª—å–≤–∏–Ω–æ–º"""
    
    print("=== –ö–æ–Ω–≤–µ—Ä—Ç–µ—Ä —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä ===")
    print("–î–æ—Å—Ç—É–ø–Ω—ã–µ —Ñ–æ—Ä–º–∞—Ç—ã:")
    print("C - –¶–µ–ª—å—Å–∏–π")
    print("F - –§–∞—Ä–µ–Ω–≥–µ–π—Ç") 
    
    try:
        # –ü–æ–ª—É—á–∞–µ–º –∏—Å—Ö–æ–¥–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ
        temp = float(input("–í–≤–µ–¥–∏—Ç–µ —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä—É: "))
        from_unit = input("–ò–∑ –∫–∞–∫–æ–π —à–∫–∞–ª—ã (C/F): ").upper()
        to_unit = input("–í –∫–∞–∫—É—é —à–∫–∞–ª—É –∫–æ–Ω–≤–µ—Ä—Ç–∏—Ä–æ–≤–∞—Ç—å (C/F): ").upper()
        
        # –ö–æ–Ω–≤–µ—Ä—Ç–∏—Ä—É–µ–º –≤ –¶–µ–ª—å—Å–∏–π –¥–ª—è –ø—Ä–æ–º–µ–∂—É—Ç–æ—á–Ω—ã—Ö —Ä–∞—Å—á–µ—Ç–æ–≤
        if from_unit == 'C':
            celsius = temp
        elif from_unit == 'F':
            celsius = (temp - 32) * 5/9
        else:
            print("‚ùå –ù–µ–≤–µ—Ä–Ω–∞—è –∏—Å—Ö–æ–¥–Ω–∞—è —à–∫–∞–ª–∞!")
            return
        
        # –ö–æ–Ω–≤–µ—Ä—Ç–∏—Ä—É–µ–º –∏–∑ –¶–µ–ª—å—Å–∏—è –≤ —Ü–µ–ª–µ–≤—É—é —à–∫–∞–ª—É
        if to_unit == 'C':
            result = celsius
        elif to_unit == 'F':
            result = (celsius * 9/5) + 32
        else:
            print("‚ùå –ù–µ–≤–µ—Ä–Ω–∞—è —Ü–µ–ª–µ–≤–∞—è —à–∫–∞–ª–∞!")
            return
        
        # –í—ã–≤–æ–¥–∏–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç
        print(f"üî¢ {temp}¬∞{from_unit} = {result:.2f}¬∞{to_unit}")
        
    except ValueError:
        print("‚ùå –û—à–∏–±–∫–∞: –ø–æ–∂–∞–ª—É–π—Å—Ç–∞, –≤–≤–æ–¥–∏—Ç–µ —á–∏—Å–ª–∞ –¥–ª—è —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä—ã!")

# –ó–∞–ø—É—Å–∫–∞–µ–º –∫–æ–Ω–≤–µ—Ä—Ç–µ—Ä
